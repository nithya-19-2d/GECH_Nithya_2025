package dependency_injection_injava;

public class Address1 {
	
	public String city;
	public String state;
	
	

	
	

	public Address1(String city, String state) {
		super();
		this.city = city;
		this.state = state;
	}



	public String getCity() {
		return city;
	}

	

	public String getState() {
		return state;
	}


}
