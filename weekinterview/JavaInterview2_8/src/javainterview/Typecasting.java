package javainterview;

public class Typecasting {
	
	public static void main(String[] args) {
		
		/*typecasting double value to int , its called explicit typcasting ,
		 *  in this we have to typecasting the value of double into integer value*/
		
		double a = 20;
		int b = (int) a;
		
		System.out.println("The value of a in double: " +a);
		System.out.println("The value of b in integer: " +b);
	}

}
